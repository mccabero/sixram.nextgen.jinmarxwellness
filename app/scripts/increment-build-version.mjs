import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, "..");
const versionPath = path.join(rootDir, "version.json");
const generatedDir = path.join(rootDir, "src", "generated");
const generatedPath = path.join(generatedDir, "appVersion.ts");

const defaultVersion = {
  major: 1,
  minor: 0,
  patch: 0,
  build: 0,
};

async function readVersion() {
  try {
    const rawVersion = await readFile(versionPath, "utf8");
    const parsedVersion = JSON.parse(rawVersion);

    return {
      major: Math.max(Number(parsedVersion.major) || defaultVersion.major, 1),
      minor: Number(parsedVersion.minor) || defaultVersion.minor,
      patch: Number(parsedVersion.patch) || defaultVersion.patch,
      build: Number(parsedVersion.build) || defaultVersion.build,
    };
  } catch {
    return defaultVersion;
  }
}

const version = await readVersion();
const nextVersion = {
  ...version,
  build: version.build + 1,
};
const versionLabel = `v${nextVersion.major}.${nextVersion.minor}.${nextVersion.patch}.${nextVersion.build}`;

await mkdir(generatedDir, { recursive: true });
await writeFile(versionPath, `${JSON.stringify(nextVersion, null, 2)}\n`, "utf8");
await writeFile(
  generatedPath,
  [
    "// This file is generated by scripts/increment-build-version.mjs.",
    "// Do not edit manually; run npm run build to update the build number.",
    "",
    "export const APP_VERSION_PARTS = {",
    `  major: ${nextVersion.major},`,
    `  minor: ${nextVersion.minor},`,
    `  patch: ${nextVersion.patch},`,
    `  build: ${nextVersion.build},`,
    "} as const;",
    "",
    `export const APP_VERSION = "${versionLabel}";`,
    "",
  ].join("\n"),
  "utf8",
);

console.log(`App version updated to ${versionLabel}`);
